    <section class="product-common-section">
        <a name="faqs"></a>
        <h3>Frequently Asked Questions</h3>
        <div class="product-common-faq-container">
            <div class="product-common-faq-inner">
                <h5>What is the most important thing to keep in mind while making a CV?</h5>
                <p>Employers or anyone trying to onboard you on their professional or academic team will not spend more than 30 seconds on your CV. So the CV must be engaging, conveying the most relevant information in a clear, accessible way. You must make
                    sure the reader finds what he wants to find ASAP focussing on their requirements and highlighting them.</p>
                <h5>What are the kinds of CV?</h5>
                <p><b>Traditional CV'</b>- These are generic in nature and have a balance of your academic and professional experiences and may not be highly focussed towards a particular sector or employer.<br/> The traditional or Reverse chronological
                    CV is the most commonly used format. It lists your education, experience and additional achievements first.<br/><br/> Ideally the following sections are included in this format:-<br/> I) Personal information<br/> II) Education <br/>III)
                    Experience-the core of your Cv <br/>IV) Additional Skills <br/>V) Interests <br/><br/><b>Skill based CV</b> <br/>In this kind of CV the focus is largely on specific skills and the information is arranged under headers of different
                    skill categories as per your employability and employers specific needs. <br/><br/><b>Academic CV's</b> <br/>As the name indicates this format focuses purely on your academic achievements and inclinations. Focus of this CV is to highlight
                    your research/academic focus and how it would suit a prospective employer or educational institution.
                </p>
            </div>
            <div class="product-common-faq-inner">
                <h5>What is the functional theme you use to draft a CV?</h5>
                <p>Our experts use custom built tool kits to break down each set of academic / professional experience into three parts I.e.<br/> a) Your Role <br/>b) Skills used in that role <br/>c) Result achieved. <br/><br/>Using this iterative format
                    the resulting output is very concise and targets the readers interests.</p>
                <h5>How long does it take to draft a comprehensive yet strategically focussed cv?</h5>
                <p>On an average our experts devote 6-7 man hours on the iteration of each individual CV and 3-4 for drafting it.</p>
                <h5>What are the key things you focus on while writing our CV?</h5>
                <p><b>Being concise</b>- All information should be concise and not repetitive<br/><br/> <b>Targeted CV content</b>- Content we put out is based on a key word mapping of the readers needs. <br/><br/><b>Priority to evidence</b>- We focus on
                    bringing out evidence of result instead of long stories.<br/><br/> <b>Clarity and white space</b>- We maintain a 60-40 ration of white space and written content
                </p>
                <h5>How long should a CV be?</h5>
                <p>Sometimes a recruiter or educational institute highlights the prescribed length but most of the times the length should not exceed 2 pages and ideally 1 page.</p>
            </div>
